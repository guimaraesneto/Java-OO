public class Variaveis{
	public static void main(String args[]){
		String nome = "Guimarães";
		int idade = 33;
		Double salario = 3500.00;

		System.out.println("+--------------------+");
		System.out.println("|       Dados        |");
		System.out.println("+--------------------+");
		System.out.println("    "+nome);
		System.out.println("    "+idade);
		System.out.println("    "+salario);
		System.out.println("");
		System.out.println("+--------------------+");
	}
}